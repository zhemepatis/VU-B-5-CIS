# 1. Find a secret that was shared using Shamir schema with t = 3
# p is 3138428376749
# x = [x1, x2, x3] = [330390789287, 1107687307693, 1673592647660]
# s = [s1, s2, s3] = [1886046246281, 2278324815846, 35014907114]

t = 3
p = 3138428376749
X = [330390789287, 1107687307693, 1673592647660]
S = [1886046246281, 2278324815846, 35014907114]

f_0 = S[0] * X[1] * X[2] * pow((X[0] - X[1]) * (X[0] - X[2]), -1, p)
f_1 = S[1] * X[0] * X[2] * pow((X[1] - X[0]) * (X[1] - X[2]), -1, p)
f_2 = S[2] * X[0] * X[1] * pow((X[2] - X[0]) * (X[2] - X[1]), -1, p)

result = (f_0 + f_1 + f_2) % p
print(result)

